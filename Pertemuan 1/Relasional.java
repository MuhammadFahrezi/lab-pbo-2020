public class Relasional {
    public static void main(String args[]) {
    int a = 10;
    int b = 20;
    System.out.println("a == b = " + (a == b) );
    System.out.println("a != b = " + (a != b) );
    System.out.println("a > b = " + (a > b) );
    System.out.println("a < b = " + (a < b) );
    System.out.println("b >= a = " + (b >= a) );
    System.out.println("b <= a = " + (b <= a) );
    }
   }

   /* 
   1. a == b (Nilai a sama dengan nilai b maka False)
   2. a != b (Nilai a tidak sama dengan nilai b maka True)
   3. a > b (Nilai a lebih besar dari nilai b maka False)
   4. a < b (Nilai a lebih kecil dari nilai b maka True)
   5. b >= a (Nilai b lebih besar sama dengan nilai a maka True)
   6. b <= a (Nilai b lebih kecil sama dengan nilai a maka False)
   */